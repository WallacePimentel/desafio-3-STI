package com.wallacepimentel.desafio3.utils;

public class Key {
    private String key1;
    private String key2;

    public Key (String key1, String key2) {
        this.key1 = key1;
        this.key2 = key2;
    }

    public String getKey1() {
        return key1;
    }

    public String getKey2() {
        return key2;
    }
}
